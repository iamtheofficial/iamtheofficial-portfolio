const spotlightButtons = document.querySelectorAll(".spotlight-btn");
const filterButtons = document.querySelectorAll(".filter-btn");
const year = document.querySelector("#year");
let activeIframe = null;

if (year) {
  year.textContent = new Date().getFullYear();
}

function createElement(tag, className, text) {
  const element = document.createElement(tag);

  if (className) {
    element.className = className;
  }

  if (text) {
    element.textContent = text;
  }

  return element;
}

function createYoutubeTrigger(credit) {
  const button = createElement("button", "youtube-embed-trigger");
  const thumbnailUrl = `https://img.youtube.com/vi/${credit.youtubeId}/hqdefault.jpg`;

  button.type = "button";
  button.setAttribute("aria-label", `Play ${credit.artist} - ${credit.title}`);
  button.style.setProperty("--thumbnail", `url('${thumbnailUrl}')`);

  const playIcon = createElement("span", "youtube-play-icon", "▶");
  playIcon.setAttribute("aria-hidden", "true");
  button.append(playIcon);

  button.addEventListener("click", () => {
    const iframe = document.createElement("iframe");
    iframe.className = "youtube-player";
    iframe.loading = "lazy";
    iframe.src = `https://www.youtube-nocookie.com/embed/${credit.youtubeId}?autoplay=1&rel=0`;
    iframe.title = `${credit.artist} - ${credit.title}`;
    iframe.allow =
      "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share";
    iframe.allowFullscreen = true;

    if (activeIframe && activeIframe !== iframe) {
      activeIframe.replaceWith(createYoutubeTrigger(activeIframe.__creditData));
    }

    iframe.__creditData = credit;
    activeIframe = iframe;
    button.replaceWith(iframe);
  });

  return button;
}

function createMedia(credit) {
  const media = createElement("div", "credit-media");

  if (credit.youtubeId) {
    media.append(createYoutubeTrigger(credit));
    return media;
  }

  const placeholder = createElement("div", "credit-placeholder");
  const image = document.createElement("img");
  image.src = "assets/images/west-west-vol-1.jpg";
  image.alt = credit.title;
  image.loading = "lazy";
  image.width = 1200;
  image.height = 1200;
  placeholder.append(image);
  media.append(placeholder);

  return media;
}

function createBadges(credit) {
  const wrapper = createElement("div", "credit-meta");

  credit.badges?.forEach((badge) => {
    const badgeEl = createElement(
      "span",
      `credit-badge credit-badge-${badge.type}`,
      badge.label.trim(),
    );
    wrapper.append(badgeEl);
  });

  return wrapper;
}

function createLinks(credit) {
  const wrapper = createElement("div", "credit-links");

  credit.links?.forEach((link) => {
    const anchor = createElement("a", null, link.label);
    anchor.href = link.url;
    anchor.target = "_blank";
    anchor.rel = "noopener noreferrer";
    wrapper.append(anchor);
  });

  return wrapper;
}

function createCreditCard(credit) {
  const article = createElement("article", "credit-card reveal");
  const content = createElement("div", "credit-content");

  const artist = createElement("h4", null, credit.artist);
  const title = createElement("h3", "credit-title", credit.title);

  article.append(createMedia(credit));
  content.append(createBadges(credit), artist, title, createLinks(credit));
  article.append(content);

  return article;
}

function getFilteredCredits(filter = "all") {
  if (filter === "all") {
    return credits;
  }

  return credits.filter((credit) =>
    credit.badges?.some((badge) => badge.type === filter),
  );
}

function revealElement(element) {
  element.classList.add("visible");
}

const revealObserver =
  "IntersectionObserver" in window
    ? new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              revealElement(entry.target);
              revealObserver.unobserve(entry.target);
            }
          });
        },
        { threshold: 0.12 },
      )
    : null;

function observeRevealElements(scope = document) {
  scope.querySelectorAll(".reveal").forEach((element) => {
    if (revealObserver) {
      revealObserver.observe(element);
    } else {
      revealElement(element);
    }
  });
}

function renderSpotlight() {
  if (!spotlightCarousel) return;

  spotlightCarousel.replaceChildren(
    ...getSpotlightCredits().map(createCreditCard),
  );

  observeRevealElements(spotlightCarousel);
}

function renderCredits(filter = "all") {
  if (!creditsGrid) return;

  activeIframe = null;
  creditsGrid.replaceChildren(
    ...getFilteredCredits(filter).map(createCreditCard),
  );
  observeRevealElements(creditsGrid);
}

filterButtons.forEach((button) => {
  button.addEventListener("click", () => {
    filterButtons.forEach((btn) => btn.classList.remove("active"));
    button.classList.add("active");
    renderCredits(button.dataset.filter);
  });
});

const spotlightItems = [
  { artist: "Jaro Local", title: "Kativa" },
  { artist: "Uncle Is", title: "Tender" },
  { artist: "Uncle Is", title: "Now That I" },
  { artist: "Roann", title: "Give You Everything" },
];

let spotlightIndex = 0;

function getSpotlightCredits() {
  return spotlightItems
    .map((item) =>
      credits.find(
        (credit) =>
          credit.artist.toLowerCase() === item.artist.toLowerCase() &&
          credit.title.toLowerCase() === item.title.toLowerCase(),
      ),
    )
    .filter(Boolean);
}

function getCarouselOffset(index, activeIndex, total) {
  let offset = index - activeIndex;

  if (offset > total / 2) {
    offset -= total;
  }

  if (offset < -total / 2) {
    offset += total;
  }

  return offset;
}

function updateSpotlightCarousel() {
  if (!spotlightCarousel) return;

  const slides = spotlightCarousel.querySelectorAll(".spotlight-slide");
  const total = slides.length;

  slides.forEach((slide, index) => {
    const offset = getCarouselOffset(index, spotlightIndex, total);

    slide.classList.remove("is-active", "is-prev", "is-next", "is-hidden");

    if (offset === 0) {
      slide.classList.add("is-active");
    } else if (offset === -1) {
      slide.classList.add("is-prev");
    } else if (offset === 1) {
      slide.classList.add("is-next");
    } else {
      slide.classList.add("is-hidden");
    }
  });
}

function renderSpotlight() {
  if (!spotlightCarousel) return;

  const spotlightCredits = getSpotlightCredits();

  spotlightCarousel.innerHTML = "";

  spotlightCredits.forEach((credit, index) => {
    const card = createCreditCard(credit);

    card.classList.add("spotlight-slide");
    card.dataset.spotlightIndex = index;

    card.addEventListener("click", () => {
      spotlightIndex = index;
      updateSpotlightCarousel();
    });

    spotlightCarousel.appendChild(card);
  });

  updateSpotlightCarousel();
  observeRevealElements(spotlightCarousel);
}

function moveSpotlight(direction) {
  const total = getSpotlightCredits().length;

  if (direction === "next") {
    spotlightIndex = (spotlightIndex + 1) % total;
  }

  if (direction === "prev") {
    spotlightIndex = (spotlightIndex - 1 + total) % total;
  }

  updateSpotlightCarousel();
}

spotlightButtons.forEach((button) => {
  button.addEventListener("click", () => {
    moveSpotlight(button.dataset.direction);
  });
});

document
  .querySelectorAll(".section-heading, .about-card, .site-footer")
  .forEach((element) => element.classList.add("reveal"));

observeRevealElements();
renderSpotlight();
renderCredits();
